async def init_db():
    """–ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö —Å –Ω–æ–≤–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–π"""
    async with aiosqlite.connect(DB_PATH) as db:
        # –°–æ–∑–¥–∞—ë–º —Ç–∞–±–ª–∏—Ü—É –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–π —Å —Ñ–ª–∞–≥–æ–º used
        await db.execute("""
            CREATE TABLE IF NOT EXISTS affirmations (
                id INTEGER PRIMARY KEY,
                text TEXT NOT NULL,
                image_id INTEGER DEFAULT 1,
                used INTEGER DEFAULT 0
            )
        """)
        
        # –¢–∞–±–ª–∏—Ü–∞ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è
        await db.execute("""
            CREATE TABLE IF NOT EXISTS schedule (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                post_time TEXT NOT NULL UNIQUE
            )
        """)
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–∏
        cursor = await db.execute("SELECT COUNT(*) FROM affirmations")
        count = (await cursor.fetchone())[0]
        
        if count == 0:
            # –ó–∞–ø–æ–ª–Ω—è–µ–º –±–∞–∑—É
            for i, text in enumerate(AFFIRMATIONS, start=1):
                await db.execute(
                    "INSERT INTO affirmations (id, text, image_id, used) VALUES (?, ?, ?, 0)",
                    (i, text, i)
                )
            logger.info(f"–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –∑–∞–ø–æ–ª–Ω–µ–Ω–∞ {len(AFFIRMATIONS)} –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—è–º–∏")
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞—Å–ø–∏—Å–∞–Ω–∏–µ
        cursor = await db.execute("SELECT COUNT(*) FROM schedule")
        sched_count = (await cursor.fetchone())[0]
        
        if sched_count == 0:
            await db.execute("INSERT INTO schedule (post_time) VALUES ('08:00')")
        
        await db.commit()


async def get_next_affirmation() -> dict:
    """
    –ü–æ–ª—É—á–∏—Ç—å —Å–ª–µ–¥—É—é—â—É—é —Å–ª—É—á–∞–π–Ω—É—é –Ω–µ–∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—É—é –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—é.
    –ö–æ–≥–¥–∞ –≤—Å–µ 500 –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω—ã, —Å–±—Ä–∞—Å—ã–≤–∞–µ—Ç —Ñ–ª–∞–≥–∏ –∏ –Ω–∞—á–∏–Ω–∞–µ—Ç –Ω–æ–≤—ã–π –∫—Ä—É–≥.
    """
    async with aiosqlite.connect(DB_PATH) as db:
        # 1. –ü—ã—Ç–∞–µ–º—Å—è –≤–∑—è—Ç—å —Å–ª—É—á–∞–π–Ω—É—é –Ω–µ–∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—É—é
        cursor = await db.execute("""
            SELECT id, text, image_id
            FROM affirmations
            WHERE used = 0
            ORDER BY RANDOM()
            LIMIT 1
        """)
        row = await cursor.fetchone()
        
        # 2. –ï—Å–ª–∏ –Ω–µ –æ—Å—Ç–∞–ª–æ—Å—å –Ω–∏ –æ–¥–Ω–æ–π (–≤—Å–µ 500 –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω—ã) ‚Äî –æ–±–Ω—É–ª—è–µ–º used –∏ –±–µ—Ä—ë–º —Å–Ω–æ–≤–∞
        if not row:
            logger.info("–í—Å–µ 500 –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–π –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω—ã! –ù–∞—á–∏–Ω–∞–µ–º –Ω–æ–≤—ã–π –∫—Ä—É–≥.")
            await db.execute("UPDATE affirmations SET used = 0")
            await db.commit()
            
            cursor = await db.execute("""
                SELECT id, text, image_id
                FROM affirmations
                ORDER BY RANDOM()
                LIMIT 1
            """)
            row = await cursor.fetchone()
        
        aff_id, text, img_id = row
        
        # 3. –ü–æ–º–µ—á–∞–µ–º –≤—ã–±—Ä–∞–Ω–Ω—É—é –∫–∞–∫ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—É—é
        await db.execute(
            "UPDATE affirmations SET used = 1 WHERE id = ?",
            (aff_id,)
        )
        await db.commit()
        
        logger.info(f"–í—ã–±—Ä–∞–Ω–∞ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏—è #{aff_id}")
        return {"id": aff_id, "text": text, "image_id": img_id or 1}


async def get_affirmation_photo(aff_id: int) -> str:
    """–ü–æ–ª—É—á–∏—Ç—å –ø—É—Ç—å –∫ —Ñ–æ—Ç–æ –∞—Ñ—Ñ–∏—Ä–º–∞—Ü–∏–∏ –∏–ª–∏ —Å–æ–∑–¥–∞—Ç—å –∑–∞–≥–ª—É—à–∫—É"""
    path = IMAGES_DIR / f"{aff_id}.png"
    if path.exists():
        return str(path)
    
    fallback_noone_path = IMAGES_DIR / "noone.png"
    if fallback_noone_path.exists():
        return str(fallback_noone_path)
    
    # –°–æ–∑–¥–∞—ë–º fallback –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ
    img = Image.new('RGB', (1080, 1080), color=(20, 20, 20))
    draw = ImageDraw.Draw(img)
    
    try:
        font = ImageFont.truetype("/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf", 80)
    except:
        font = ImageFont.load_default()
    
    text = "STERVAFIT"
    bbox = draw.textbbox((0, 0), text, font=font)
    text_width = bbox[2] - bbox[0]
    text_height = bbox[3] - bbox[1]
    position = ((1080 - text_width) // 2, (1080 - text_height) // 2)
    
    draw.text(position, text, fill="white", font=font)
    img.save(fallback_noone_path)
    
    return str(fallback_noone_path)


